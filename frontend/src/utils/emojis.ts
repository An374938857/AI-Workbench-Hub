export interface EmojiCategory {
  name: string
  emojis: string[]
}

function uniqueKeepOrder(items: string[]): string[] {
  const seen = new Set<string>()
  const out: string[] = []
  for (const item of items) {
    if (seen.has(item)) continue
    seen.add(item)
    out.push(item)
  }
  return out
}

function normalizeCategories(categories: EmojiCategory[]): EmojiCategory[] {
  const globalSeen = new Set<string>()
  return categories.map((cat) => {
    const deduped: string[] = []
    for (const emoji of uniqueKeepOrder(cat.emojis)) {
      if (globalSeen.has(emoji)) continue
      globalSeen.add(emoji)
      deduped.push(emoji)
    }
    return { ...cat, emojis: deduped }
  })
}

